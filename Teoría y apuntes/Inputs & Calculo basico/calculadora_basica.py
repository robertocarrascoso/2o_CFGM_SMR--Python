primerNum = int(input('Indique el primer número: '))
segundoNum = int(input('Indique el segundo número: '))

suma = primerNum + segundoNum
resta = primerNum - segundoNum
multiplicacion = primerNum * segundoNum
media = suma / 2

print('Suma de los datos: ', suma)
print('Resta de los datos: ', resta)
print('Multiplicación de los datos: ', multiplicacion)
print('Media de los datos: ', media)